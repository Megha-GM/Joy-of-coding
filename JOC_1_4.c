//C program to check size of various data types
#include <stdio.h>
int main()
{
    char a;
    short int b;
    float c;
    double d;
    printf("Size of character data type is %ld \n",sizeof(a));
    printf("Size of integer data type is %ld \n",sizeof(b));
    printf("Size of float data type is %ld \n",sizeof(c));
    printf("Size of double data type is %ld \n",sizeof(d));
    return 0;
}


