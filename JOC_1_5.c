//C program to check bitwise operators
#include <stdio.h>
int main()
{
   int num=22;
   printf("Left shift by one position = %d\n",num<<1);
   printf("Right shift by one position = %d\n",num>>1);
   printf("Left shift by two positions = %d\n",num<<2);
   printf("Right shift by two positions = %d\n",num>>2);
   return 0;
}


